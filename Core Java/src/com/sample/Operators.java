package com.sample;

public class Operators {

	public static void main(String[] args) {
		int x = 5;
		System.out.println(x--);	//5
		System.out.println(--x);	//3
		System.out.println(x++);	//3
		System.out.println(++x);	//5
		System.out.println("Hello" + "World");	//HelloWorld
		System.out.println(5+3);	//8
		System.out.println(15%2);	//1
	}

}
