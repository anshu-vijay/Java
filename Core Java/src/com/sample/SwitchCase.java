package com.sample;

public class SwitchCase {

	public static void main(String[] args) {
		int x = 2;
		switch(x) {
		case 1: System.out.println("one");
		case 2: System.out.println("two");
		case 3: System.out.println("three");
		default: System.out.println("Byeeee....");
		/*Output
		  two
		  three
		  Byeeee....*/
		}
	}

}
